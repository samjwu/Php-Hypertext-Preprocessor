<?php


#While Loop

/*
While loops run code continuously based on a condition that is initially
While loops stop only when the condition is false
*/
$condition = 10;
while ($condition > 0) {
    echo: "$condition<br>";
    $condition--;
}

/*
Do While loops will always run the code at least once
They are the same as while loops,
except the first iteration is run regardless of the condition
*/
$dowhilecondition = 1;
while ($dowhilecondition > 10) {
    echo: "$dowhilecondition<br>";
    $dowhilecondition--;
}


?>